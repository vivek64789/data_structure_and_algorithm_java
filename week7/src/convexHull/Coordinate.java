package convexHull;

public class Coordinate {
	//creating 2 partition in memory block
	int x, y; 
	
	// defining the constructor so that this memory is allocated when class is called
	Coordinate(int x, int y){ 
		
		// assigning the input value to the value of class variable
		this.x = x; 
		this.y = y;
	}
}